    private void jButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveActionPerformed
        try{     
            
            int row = jTablePolaznici.getSelectedRow();
            String value = (jTablePolaznici.getModel().getValueAt(row,0).toString());
            
            Connection conn = MySQLConnection.getInstance().getConnection();
            PreparedStatement stmt = null;
            
            String query = "UPDATE polaznici SET ime = ?,prezime = ?,pol = ?,broj_telefona = ?,grupa = ? ,vrsta_treninga = ?,trener = ?,images = ?"
                        + " WHERE id =" + value;
            stmt = conn.prepareStatement(query);
            
            stmt.setString(1, jTextFieldIme.getText());
            stmt.setString(2, jTextFieldPrezime.getText());
            String pol = "";
            
            if (jRadioButtonMuski.isSelected()){
                pol = "Muski";
            }
            if (jRadioButtonZenski.isSelected()){
                pol = "Zenski";
            }
            stmt.setString(3, pol);
            stmt.setString(4, jTextFieldTelefon.getText());
            String grupa = "";
            
            if (jRadioButtonGrupni.isSelected()){
                grupa = "grupni";
            }
            if (jRadioButtonIndividualni.isSelected()){
                grupa = "individualni";
            }
            stmt.setString(5, grupa);
            stmt.setString(6, jComboBox1.getSelectedItem().toString());
            stmt.setString(7, jTextFieldTrener.getText());
            stmt.setBytes(8, person_image);
            
            stmt.executeUpdate();
            
             JOptionPane.showMessageDialog(null,"Uspesno ste izmenili podatke");
            
            
             }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        } 
                                                 
    }//GEN-LAST:event_jButtonSaveActionPerformed

