     private void grupioriindividualni(){
        
         
        if (jRadioGrupni.isSelected()){
             grupaG= "grupni_polaznici";
        }
     if (jRadioIndividualni.isSelected()){
             grupaG = "individualni_polaznici";
     }
        try{
             String query = "SELECT * FROM "  + grupaG ;
             Connection conn = MySQLConnection.getInstance().getConnection();
              PreparedStatement pst = conn.prepareStatement(query);
               ResultSet rs = pst.executeQuery();
            DefaultTableModel model = (DefaultTableModel) jTablePolaznici.getModel();
            model.setRowCount(0);
             while (rs.next()) {
             Object[] row = new Object[9]; 
             row[0] = rs.getInt("Id");
             row[1] = rs.getString("ime");
             row[2] = rs.getString("prezime");
             row[3] = rs.getString("pol");
             row[4] = rs.getString("broj_telefona");
             row[5] = rs.getString("grupa");
             row[6] = rs.getString("vrsta_treninga");
             row[7] = rs.getString("trener");
             row[8] = rs.getString("date");
             model.addRow(row);
            
    }
    
    rs.close();
    conn.close();
} catch (Exception ex) {
            System.out.println("Greska" + ex.getLocalizedMessage());
}
     }            
              

