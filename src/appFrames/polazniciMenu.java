    private void jButtonChangeImageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonChangeImageActionPerformed
        
    JFileChooser fileChooser = new JFileChooser();
    int result = fileChooser.showOpenDialog(this);
    if (result == JFileChooser.APPROVE_OPTION) {
        File selectedFile = fileChooser.getSelectedFile();
        
        try {
            byte[] imageData = Files.readAllBytes(selectedFile.toPath());
            
            String sql = "UPDATE images SET image_data = ? WHERE username = ?";
            Connection conn = MySQLConnection.getInstance().getConnection();
            PreparedStatement pst3 = conn.prepareStatement(sql);
            pst3.setBytes(1, imageData);
            pst3.setString(2, username); 
            int rowsUpdated = pst3.executeUpdate();
            
            if (rowsUpdated > 0) {
                ImageIcon imageIcon = new ImageIcon(imageData);
                Image image = imageIcon.getImage();
                Image scaledImage = image.getScaledInstance(jLabelProfilna.getWidth(), jLabelProfilna.getHeight(), Image.SCALE_SMOOTH);
                jLabelProfilna.setIcon(new ImageIcon(scaledImage));
                JOptionPane.showMessageDialog(this, "Profile image updated successfully!");
            } else {
                JOptionPane.showMessageDialog(this, "Failed to update profile image.");
            }
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "An error occurred while updating profile image.");
        }
    }

    }//GEN-LAST:event_jButtonChangeImageActionPerformed

