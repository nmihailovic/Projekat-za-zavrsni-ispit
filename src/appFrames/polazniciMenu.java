    public void polaznicitabela(){
        
        
  try {
        String query = "SELECT grupa, trener, vrsta_treninga, date FROM polaznici WHERE username = ?";
        Connection conn = MySQLConnection.getInstance().getConnection();
        PreparedStatement pst = conn.prepareStatement(query);
        pst.setString(1, username);

        
        ResultSet rs = pst.executeQuery();

        DefaultTableModel model = (DefaultTableModel) jTablePolaznik.getModel();
       
        while (rs.next()) {
            Object[] row = new Object[4];
            row[0] = rs.getString("grupa");
            row[1] = rs.getString("trener");
            row[2] = rs.getString("vrsta_treninga");
            row[3] = rs.getString("date");
            model.addRow(row);
        }

        rs.close();
        pst.close();
        conn.close();

    } catch (SQLException ex) {
        System.out.println("Error: " + ex.getMessage());
    }
}
